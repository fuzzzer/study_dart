// Dart function that takes two numbers as arguments and returns their product:

int product(int num1, int num2) {
  return num1 * num2;
}

// In this function, the product function takes two int arguments, num1 and num2.
// It then calculates the product of num1 and num2 using the * operator and returns
// the result using the return keyword.
// This function can be used in a Dart program by calling it with two arguments:

void main() {
  var result = product(3, 4);
  print("The product of 3 and 4 is $result");
}
// In this example, the main function calls the product function with arguments 3 and 4,
// and stores the result in the result variable. Finally, it prints the result to the console.
