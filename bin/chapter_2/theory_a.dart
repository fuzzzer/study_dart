void main(List<String> args) {
//--------------------------------------------------------------
// If-else statements:
// If-else statements are used to execute different blocks of code based on whether a certain condition is true or false.
// Example:

  int score = 70;

  if (score >= 90) {
    print("You got an A!");
  } else if (score >= 80) {
    print("You got a B!");
  } else if (score >= 70) {
    print("You got a C!");
  } else {
    print("You got a D or below.");
  }

//--------------------------------------------------------------

// For loops:
// For loops are used to repeat a block of code multiple times.
// Example:

  for (int i = 1; i <= 10; i++) {
    print(i);
  }

//--------------------------------------------------------------

// While loops:
// While loops are used to repeat a block of code until a certain condition is met.
// Example:

  int i = 1;
  while (i <= 10) {
    print(i);
    i++;
  }

//--------------------------------------------------------------

// Note: These are just simple examples to illustrate the concepts. The actual usage may vary based on the program requirements.
}
