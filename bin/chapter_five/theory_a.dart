// Inheritance is a fundamental concept in object-oriented 
// programming (OOP) that allows a new class to inherit properties and
// behaviors of an existing class, referred to as the base class or superclass.
// This allows developers to create a new class that reuses and extends the functionality
// of an existing class, reducing code duplication and making it easier to maintain the code.

// Polymorphism, on the other hand, is the ability of a single function, method,
// or operator to work with objects of different classes. In other words, polymorphism
// allows you to write code that can work with objects of different types,
// but behave differently based on their type.

// For example, you can have a base class named Shape with a method named area that
// calculates the area of a shape. Then, you can create subclasses such as Rectangle
// and Circle that inherit from the Shape class and each of them implements its own version of the area method,
// specific to their own type.

// In this way, you can use polymorphism to write code that works with objects of
// different shapes and calculates their area, without needing to know the exact type of the object.
// This makes your code more flexible, maintainable, and reusable.




